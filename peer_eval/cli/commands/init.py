"""
'peer-eval init' command — initialize project configuration.

Creates a .peer-eval.toml template in the current directory.
"""

import os
import sys
from argparse import ArgumentParser, Namespace
from pathlib import Path

from .base import BaseCommand


class InitCommand(BaseCommand):
    """Initialize .peer-eval.toml for the current project."""

    @property
    def name(self) -> str:
        return "init"

    @property
    def help(self) -> str:
        return "Initialize .peer-eval.toml configuration file"

    def register(self, subparsers) -> None:
        """Register the init subcommand."""
        parser = subparsers.add_parser(
            self.name,
            help=self.help,
            description="Create a starter .peer-eval.toml in the current directory"
        )
        parser.set_defaults(command_obj=self)

    def execute(self, args: Namespace) -> int:
        """Execute the init command."""
        config_path = Path(".peer-eval.toml")
        cwd_name = Path.cwd().name

        if config_path.exists():
            print(f"ℹ️  {config_path} already exists. Skipping.")
            return 0

        template = f'''# Peer-Eval Configuration
# Generated by 'peer-eval init'

[project]
id = "{cwd_name}"
repo_path = "."

[evaluation]
deadline = "2024-12-01T23:59:00Z"
# since = "2024-09-01T00:00:00Z"
# until = "2024-12-01T23:59:59Z"

[sprints]
start_date = "2024-09-01T00:00:00Z"
length_days = 15
count = 5

[llm]
mode = "dry-run"  # Options: live, dry-run, skip

[provider.gitlab]
url = "https://gitlab.com"
# project_id = ""  # Set via --project-id or environment
# ssl_verify = true

[provider.github]
url = "https://api.github.com"
# repo = ""  # Set via --repo or environment
'''

        try:
            config_path.write_text(template, encoding="utf-8")
            print(f"✓ Created {config_path}")
            print()
            print("📝 Next steps:")
            print("   1. Edit .peer-eval.toml to configure your project")
            print("   2. Set credentials in .peer-eval.env (or pass --env-file)")
            print("   3. Run: peer-eval gitlab --project-id <ID> --deadline <ISO8601>")
            print("         or: peer-eval fixture --input <file.json> --deadline <ISO8601>")
            return 0
        except Exception as e:
            print(f"❌ Failed to create {config_path}: {e}", file=sys.stderr)
            return 1
